// Write a program to find volume of cube.(Area=l*b*h)

#include <iostream>
#include <cstdlib>
using namespace std;

int main()
{
   system("CLS");
   int length = 10, breadth = 15, height = 20;
   int volume = length * breadth * height;

   cout << "Volume of cube is: " << volume << endl;
   return 0;
}